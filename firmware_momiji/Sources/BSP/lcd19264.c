uint32_t rev(uint32_t in)
{
	uint8_t swap;
	uint8_t swap1, swap2, swap3, swap4;
	uint32_t ret;
	
	swap1 = in >> 24;
	swap2 = in >> 16;
	swap3 = in >> 8;
	swap4 = in >> 0;
	
	swap = swap1;
	swap1 = swap4;
	swap4 = swap;
	
	swap = swap2;
	swap2 = swap3;
	swap3 = swap;
	
	ret = swap1;
	ret <<= 8;
	ret |= swap2;
	ret <<= 8;
	ret |= swap3;
	ret <<= 8;
	ret |= swap4;
	
	return ret;
}


