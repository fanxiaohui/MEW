void LCD_DrawPoint(uint8_t x, uint8_t y, uint8_t c)
{
	lcd_ram[y][x] = c;
}

void LCD_DRAW_FRAME(void)
{
	uint8_t x, y, c;

	for(y = 0; y < 8; y ++)
	{
		for(x = 0; x < 192; x ++)
		{
			c = 0;
			c |= (lcd_ram[y * 8 + 0][x] ? 1 : 0) << 7;
			c |= (lcd_ram[y * 8 + 1][x] ? 1 : 0) << 6;
			c |= (lcd_ram[y * 8 + 2][x] ? 1 : 0) << 5;
			c |= (lcd_ram[y * 8 + 3][x] ? 1 : 0) << 4;
			c |= (lcd_ram[y * 8 + 4][x] ? 1 : 0) << 3;
			c |= (lcd_ram[y * 8 + 5][x] ? 1 : 0) << 2;
			c |= (lcd_ram[y * 8 + 6][x] ? 1 : 0) << 1;
			c |= (lcd_ram[y * 8 + 7][x] ? 1 : 0) << 0;
			if(x < 64)
			{
				lcd_SetX(0, x);	
				lcd_SetY(0, y);				
				lcd_Write(0, c);
			}
			else if(x < 128)
			{
				lcd_SetX(1, x - 64);
				lcd_SetY(1, y);	
				lcd_Write(1, c);
			}
			else if(x < 192)
			{
				lcd_SetX(2, x - 128);
				lcd_SetY(2, y);	
				lcd_Write(2, c);
			}
			
		}
	}
}

